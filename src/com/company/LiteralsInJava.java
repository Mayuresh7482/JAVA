package com.company;

public class LiteralsInJava {
    public static void main(String[] args) {
        byte age = 34;
        int age2 = 56;
        short age3 = 87;
        long agedino = 5111233L;
        char ch = 'A';
        float f1 = 5.6f;
        double d1 = 4.51D;
        boolean a = true;
        String str = "Mayuresh";
        System.out.println( str + ',' + age + "," + age2 + "," + age3 + "," + agedino + "," + ch + "," + f1 + "," + d1 + "," + a);
    }
}
